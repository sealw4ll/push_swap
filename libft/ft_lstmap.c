#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*tmp;
	t_list	*cpy;

	if (!lst)
		return (NULL);
	cpy = NULL;
	while (lst)
	{
		tmp = ft_lstnew(f(lst->content));
		if (!tmp)
		{
			ft_lstclear(&tmp, del);
			return (NULL);
		}
		if (!cpy)
			cpy = tmp;
		else
			ft_lstadd_back(&cpy, tmp);
		lst = lst->next;
	}
	return (cpy);
}
